import { dateMyGlobeMapPacks } from './dateMyGlobeMapPacks.ts';

type Position = [number, number];
type PolygonCoordinates = Position[][];
type MultiPolygonCoordinates = PolygonCoordinates[];
type LineStringCoordinates = Position[];

export interface DateMyGlobeMapFeature {
	type: 'Feature';
	properties: {
		label: string;
		role: 'context' | 'changed' | 'removed' | 'added' | 'boundary';
		labelPosition?: Position;
		labelSize?: 'small' | 'normal' | 'large';
		showLabel?: boolean;
	};
	geometry:
		| {
			type: 'Polygon';
			coordinates: PolygonCoordinates;
		}
		| {
			type: 'MultiPolygon';
			coordinates: MultiPolygonCoordinates;
		}
		| {
			type: 'Point';
			coordinates: Position;
		}
		| {
			type: 'LineString';
			coordinates: LineStringCoordinates;
		};
}

export interface DateMyGlobeMap {
	type: 'FeatureCollection';
	label: string;
	note: string;
	features: DateMyGlobeMapFeature[];
}

export interface DateMyGlobeEvent {
	id: string;
	name: string;
	year: number;
	question: string;
	beforeLabel: string;
	afterLabel: string;
	description: string;
	sourceLabel?: string;
	sourceUrl?: string;
	visualPlaceholder: {
		mode: 'name-change' | 'border-shift' | 'union' | 'split' | 'new-state';
		beforeNote: string;
		afterNote: string;
	};
	region: string;
	includeInChecklist?: boolean;
	checklistOrder?: number;
	sourceConfidence?: 'primary' | 'reference' | 'derived';
	dateWindowNote?: string;
	mapViewport?: {
		west: number;
		south: number;
		east: number;
		north: number;
	};
	beforeMap?: DateMyGlobeMap;
	afterMap?: DateMyGlobeMap;
	mapSourceNote?: string;
}

const polygon = (
	label: string,
	role: DateMyGlobeMapFeature['properties']['role'],
	coordinates: Position[],
	options: Partial<DateMyGlobeMapFeature['properties']> = {},
): DateMyGlobeMapFeature => ({
	type: 'Feature',
	properties: { label, role, ...options },
	geometry: {
		type: 'Polygon',
		coordinates: [[...coordinates, coordinates[0]]],
	},
});

const multiPolygon = (
	label: string,
	role: DateMyGlobeMapFeature['properties']['role'],
	polygons: Position[][],
	options: Partial<DateMyGlobeMapFeature['properties']> = {},
): DateMyGlobeMapFeature => ({
	type: 'Feature',
	properties: { label, role, ...options },
	geometry: {
		type: 'MultiPolygon',
		coordinates: polygons.map((coordinates) => [[...coordinates, coordinates[0]]]),
	},
});

const withChecklistMap = (event: DateMyGlobeEvent, index: number): DateMyGlobeEvent => {
	const mapData = dateMyGlobeMapPacks[event.id as keyof typeof dateMyGlobeMapPacks] as Pick<DateMyGlobeEvent, 'mapViewport' | 'beforeMap' | 'afterMap' | 'mapSourceNote'> | undefined;
	if (!mapData) {
		throw new Error(`Missing Date My Globe map pack for ${event.id}`);
	}

	return {
		...event,
		...mapData,
		includeInChecklist: true,
		checklistOrder: event.checklistOrder ?? index + 1,
		sourceConfidence: event.sourceConfidence ?? 'reference',
		mapSourceNote: mapData.mapSourceNote,
	};
};

