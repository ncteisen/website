type ReferenceEventInput = {
	id: string;
	name: string;
	year: number;
	question: string;
	beforeLabel: string;
	afterLabel: string;
	description: string;
	region: string;
	mode?: DateMyGlobeEvent['visualPlaceholder']['mode'];
	source?: keyof typeof globeDatingSources;
};

const referenceEvent = ({
	id,
	name,
	year,
	question,
	beforeLabel,
	afterLabel,
	description,
	region,
	mode = 'name-change',
	source = 'oneWorld',
}: ReferenceEventInput): DateMyGlobeEvent => {
	const sourceInfo = globeDatingSources[source];

	return {
		id,
		name,
		year,
		question,
		beforeLabel,
		afterLabel,
		description,
		sourceLabel: sourceInfo.label,
		sourceUrl: sourceInfo.url,
		visualPlaceholder: {
			mode,
			beforeNote: beforeLabel,
			afterNote: afterLabel,
		},
		region,
	};
};

